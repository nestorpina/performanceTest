package com.izg.test.json;

public class JsonResponse {

	String id;
	long time;
	
	public JsonResponse() {}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
}
